// Helper function to get blog by slug
function getBlog($slug) {
    global $blogs;
    return isset($blogs[$slug]) ? $blogs[$slug] : null;
}

// Helper function to get all blogs
function getAllBlogs() {
    global $blogs;
    return $blogs;
}

// Helper function to get blogs by category
function getBlogsByCategory($category) {
    global $blogs;
    if ($category === 'all') {
        return $blogs;
    }
    return array_filter($blogs, function($blog) use ($category) {
        return $blog['category'] === $category;
    });
}
